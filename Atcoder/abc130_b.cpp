void solve(int caseno)
{
    int n, x, i, d, l, ans = 1;

    cin >> n >> x;

    d = 0;
    for(i = 2; i <= n+1; i++)
    {
        cin >> l;
        d += l;

        if(d <= x)
            ans++;
    }

    cout << ans << "\n";
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int T = 1, caseno = 0;

//    cin >> T;

    while(T--)
    {
        solve(++caseno);
    }
}

