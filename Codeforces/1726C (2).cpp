void solve(int caseno)
{
    int n, i, ans;
    string s;

    cin >> n >> s;
    ans = n;

    for(i = 0; i+1 < 2*n; i++)
    {
        if(s[i] == ')' && s[i+1] == '(')
            ans--;
    }

    cout << ans << "\n";
}

int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);

    int T = 1, caseno = 0;

    cin >> T;

    while(T--)
    {
        solve(++caseno);
    }
}